let currentLang = localStorage.getItem('appLang') || (navigator.language.startsWith('pt') ? 'pt' : 'es');

function setLanguage(lang) {
  if (!i18nDist[lang]) lang = 'es';
  currentLang = lang;
  localStorage.setItem('appLang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (i18nDist[lang][key]) {
      // Evitar sobreescribir HTML anidado si lo hay, solo cambiamos textContent
      // aunque si hay spans internos con ID, podríamos reemplazarlos,
      // la mejor práctica acá es textContent para elementos hoja.
      // En casos especiales (como span rojo), el data-i18n se pone al nivel más bajo.
      el.textContent = i18nDist[lang][key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (i18nDist[lang][key]) {
      el.placeholder = i18nDist[lang][key];
    }
  });

  // Actualizar botones del header para mostrar idioma activo
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.dataset.lang === lang) {
      btn.classList.add('text-accent-gold', 'font-bold');
      btn.classList.remove('text-slate-400');
    } else {
      btn.classList.remove('text-accent-gold', 'font-bold');
      btn.classList.add('text-slate-400');
    }
  });
}

// Global exposure
window.I18N = {
  get: function(key) {
    return i18nDist[currentLang][key] || key;
  },
  setLang: setLanguage,
  getCurrentLang: () => currentLang
};

// Start default lang when DOM loads
document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
});
